a = int(input("nhập số a:"))

b = int(input("nhập số b:"))


print("{0}+{1}={2}".format(a, b, a + b))
print("{0}-{1}={2}".format(a, b, a - b))

print("{0}*{1}={2}".format(a, b, a * b))
print("{0}/{1}={2}".format(a, b, a / b))
# Phép chia lấy số dư2
print("{0}%{1}={2}".format(a, b, a % b))
# a mữ b
print("{0}**{1}={2}".format(a, b, a**b))
# Phép chia lấy số nguyên
print("{0}//{1}={2}".format(a, b, a // b))
